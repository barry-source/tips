import UIKit

// MARK: - UIScreen
public extension UIScreen {
    
    /// 屏幕宽度
    var screenWidth: CGFloat {
        return bounds.width
    }
    
    /// 屏幕高度
    var screenHeight: CGFloat {
        return bounds.height
    }
    
}
